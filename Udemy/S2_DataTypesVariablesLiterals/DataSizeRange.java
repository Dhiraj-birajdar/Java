//import java.lang.*;
public class DataSizeRange {
    public static void main(String[] args)
    {
        System.out.println("Int Min : "+Integer.MIN_VALUE);
        System.out.println("Int Max : "+Integer.MAX_VALUE);
        System.out.println("Int Byte : "+Integer.BYTES);
        System.out.println("Int Size : "+Integer.SIZE);

        System.out.println("Byte Min : "+Byte.MIN_VALUE);
        System.out.println("Byte Max : "+Byte.MAX_VALUE);
        System.out.println("Byte Byte : "+Byte.BYTES);
        System.out.println("Byte Size : "+Byte.SIZE);
        
        System.out.println("Float Min : "+Float.MIN_VALUE);
        System.out.println("Float Max : "+Float.MAX_VALUE);
        System.out.println("Float Byte : "+Float.BYTES);
        System.out.println("Float Size : "+Float.SIZE);

        System.out.println("\nChar Min : "+Character.MIN_VALUE);
        System.out.println("Char Max : "+Character.MAX_VALUE);
        System.out.println("Char Byte : "+Character.BYTES);
        System.out.println("Char Size : "+Character.SIZE);
    }
}
